#include "config.h"

#include "DNS.h"
#include "NotImplemented.h"
#include "WebCoreFrameBridge.h"

using namespace android;

namespace WebCore {

void prefetchDNS(const String& hostname, Frame* frame, DnsPrefetchType type)
{
    switch(type) {
        case DnsPrefetchLink:
            notImplemented();
            break;

        case DnsPrefetchResource:
            WebFrame::getWebFrame(frame)->resolveDnsForHost(hostname);
            break;

        case DnsPrefetchHistory:
            notImplemented();
            break;

        default:
            break;
    }
}

}
